char	*ft_strcpy(char *dest, char *src)
{
	int	loca;

	loca = 0;
	while (src[loca] != '\0')
	{
		dest[loca] = src[loca];
		loca++;
	}
	dest[loca] = '\0';
	return (dest);
}
