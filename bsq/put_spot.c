#include "header.h"

void	put_spot(t_shape *sh, t_ob *ob)
{
	int	y;
	int	x;

	y = sh->y;
	while (sh->y - y != sh->big)
	{
		x = sh->x;
		while (sh->x - x != sh->big)
		{
			sh->p[y][x] = ob->full;
			x--;
		}
		y--;
	}
}
